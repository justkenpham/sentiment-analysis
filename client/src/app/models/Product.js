export class Product{
    constructor(product_id, product_name, description, price, image, rating, category_id){

        this.product_id = product_id; // int
        this.product_name = product_name; //str
        this.description = description; // str
        this.price = price; // int
        this.image = image; // str
        this.rating = rating; // str
        this.category_id = category_id; // int

    }
}